# Tipos de datos

# String (o cadena de texto)
name = "John"
lastname = "Doe"
greeting = 'Hello, World!'

# Integer (o entero)
number = 2000
age = 30
quantity = 100

# Float (o número decimal)
price = 19.99
height = 1.75
temperature = 36.6

# Boolean (o booleano)
is_active = True
is_admin = False
has_access = True

# Datos especiales

# Listas (o arrays)
numbers = [1, 2, 3, 4, 5]
fruits = ["apple", "banana", "cherry"]

# Tuplas
point = (5, 15)
coordinates = (10.0, 20.0)

# Sets
unique_numbers = {1, 2, 3, 4, 5}
unique_fruits = {"apple", "banana", "cherry"}

# Diccionarios (o mapas)
person = {
    "name": "John",
    "lastname": "Doe",
    "age": 30
}
car = {
    "make": "Toyota",
    "model": "Corolla",
    "year": 2020
}

# Todo en Python es un objeto, por lo que cada dato tiene un tipo asociado
print(age)
print(type(age))

print(name.upper())
