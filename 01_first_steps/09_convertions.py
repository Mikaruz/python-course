value = 100

print("The value is:", value)
print("The type of the value is:", type(value))

# Convertir int a str
value = str(value)
print("Converted to string:", value)
print("Type after conversion to string:", type(value))

# Convertir str a int
value = int(value)
print("Converted back to integer:", value)
print("Type after conversion back to integer:", type(value))

# Convertir int a float
value = float(value)
print("Converted to float:", value)
print("Type after conversion to float:", type(value))